# Define an array (list)
my_array = [1, 2, 3, 4, 5]

# Function to append a new item to the end of the array
def append_to_array(array, new_item):
    array.append(new_item)

# New item to append
new_item = 6

# Append the new item to the array
append_to_array(my_array, new_item)

# Print the updated array
print("Updated Array:", my_array)
